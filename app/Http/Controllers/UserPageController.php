    public function changeData(request $request){

        $user = Auth::user();

        if($request->input('name')){
            $user->name = $request->input('name');
            $user->save();

        } elseif ($request->input('surname')){
            $user->surname = $request->input('surname');
            $user->save();

        } elseif ($request->input('email')){
            $user->email = $request->input('email');
            $user->save();

        } elseif ($request->input('password') && $request->input('passwordCheck')){
            if($request->input('password') == $request->input('passwordCheck')){
                $user->password = bcrypt($request->input('password'));
                $user->save();
                return redirect()->back()->with("success","Mot de passe changé !");

            } else {
                return redirect()->back()->with("error","Les mots de passe ne correspondent pas");
            }
        }

        return redirect()->back()->with("success","Informations changées avec succès");
