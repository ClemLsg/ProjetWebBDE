    public function index(){

        $ideaboxes = IdeaBox::all();
        $bestboxes = IdeaBox::with('votes')->get()->sortByDesc(function($bestboxes){
            return $bestboxes->votes->count();
        })->take(3);
        $bde = 0;
        if (Auth::check()) {
            if (Auth::user()->rank == 1){
                $bde = 1;
            }
        }

        return view('ideabox', compact("ideaboxes",'bestboxes', "bde"));
    }

    public function addidea(request $request){

        $ideabox = IdeaBox::create([
            "name" => $request->input("titre"),
            "desc" => $request->input("desc")
        ]);

        Auth::user()->propose()->save($ideabox);

        return redirect()->back()->with('success','votre idée a bien été retenue');

    }

    public function like($id) {
        if (Auth::check()) {
            if (Auth::user()->vote()->find($id)){
                Auth::user()->vote()->detach($id);
                return redirect()->back()->with('error',"Ton vote a bien été retiré");
            } else {
                Auth::user()->vote()->attach($id);
                return redirect()->back()->with('success',"Ton vote a bien été pris en compte");
            }
        }
        else {
            return redirect()->back()->with('error',"Tu n'es pas connecté");
        }
    }

