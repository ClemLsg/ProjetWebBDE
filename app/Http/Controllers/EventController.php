    public function index($id)
    {
        $event = Activitie::find($id);
        $firstparticipants = Activitie::find($id)->participants->take('6');

        return view('event', compact('event', 'firstparticipants'));
    }

    public function comment(request $request){
        $request->all();
            $comment = new Comment();
            $comment->content = $request->input('content');
            $comment->picture_id = $request->input('postid');
            $comment->save();

            Auth::user()->comments()->save($comment);

            return response()->json(['responseText' => 'Success!'], 200);
        }

    public function like(request $request){
        $request->all();

        $pict = Picture::findOrFail($request->input('postid'));

        if(!Auth::user()->likes->contains($request->input('postid'))){
            Auth::user()->likes()->attach($pict);
        }

        return response()->json(['responseText' => 'Success!'], 200);
    }

    public function sub($id){

        $event = Activitie::findOrFail($id);
        $user = Auth::user();

        $user->activities()->attach($event);


        return redirect()->back();
    }

